OR e.DIAGCODE BETWEEN 'M17' AND 'M179'
OR e.DIAGCODE BETWEEN 'G81' AND 'G839'
OR e.DIAGCODE BETWEEN 'J301' AND 'J304'
OR e.DIAGCODE BETWEEN 'G43' AND 'G439') and  HOSPCODE=11383
